# data_types.py
# Exercise 2: Practice using basic Python data types

# String: text inside quotes
country = "Ghana"
print(country)
print(type(country))

# Integer: whole number
age = 20
print(age)
print(type(age))

# Float: decimal number
price = 12.5
print(price)
print(type(price))

# Boolean: True or False value
is_learning = True
print(is_learning)
print(type(is_learning))
